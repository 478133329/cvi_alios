/**
 * @brief [YNR] Noise Profile (luma threshods & noise profile offsets)
 *
 * @param[out] NpOffsetThrBase      (Fixed point) Noise profile np_low_offset
 * @param[out] NpSlopeBase          (Fixed point) Noise profile np_slope
 * @param[in]  np                   (Floating point) Calibrated noise profile parameters
 * @param[in]  dgain                (Fixed point) ISP Dgain
 * @param[in]  wbGain_b, wbGain_r   (Fixed point) WB Bgain & Rgain
 * @param[in]  fCCM                 CCM
 * @param[in]  GammaLUT             Gamma LUT
 * @param[in]  NpLumaThrBase        Luma thresholds
 */
int cali_isp_ynr_np_calc(
	int ISO_val,
	ISP_CMOS_NOISE_CALIBRATION_S *np,
	int dgain,
	int wbGain_b, int wbGain_r,
	float *fCCM,
	uint16_t *GammaLUT,
	uint8_t *NpLumaThrBase,
	uint8_t *NpOffsetThrBase,
	int16_t *NpSlopeBase)
{
	// For noise profile interpolation
	float fCaliSlope0[ISP_BAYER_CHN_NUM] = { 0.0 }, fCaliIntercept0[ISP_BAYER_CHN_NUM] = { 0.0 };
	float fCaliSlope1[ISP_BAYER_CHN_NUM] = { 0.0 }, fCaliIntercept1[ISP_BAYER_CHN_NUM] = { 0.0 };
	float fCaliSlope[ISP_BAYER_CHN_NUM] = { 0.0 }, fCaliIntercept[ISP_BAYER_CHN_NUM] = { 0.0 };

	// Calibrated noise profile
	float fCaliSlopeB, fCaliSlopeG, fCaliSlopeR;
	float fCaliInterceptG, fCaliInterceptB, fCaliInterceptR;

	CVI_U32 *ISO_list;

	get_iso_tbl(&ISO_list);

	int iso_idx_0 = MIN(FindPower2((int)(ISO_val / 100)), 15);
	int iso_idx_1 = (iso_idx_0 >= 15) ? iso_idx_0 : iso_idx_0 + 1;
	int ISO_val_0 = ISO_list[iso_idx_0];
	int ISO_val_1 = ISO_list[iso_idx_1];

	if (iso_idx_0 < 15) {
		for (int ch = 0; ch < ISP_BAYER_CHN_NUM; ch++) {
			fCaliSlope0[ch] = np->CalibrationCoef[iso_idx_0][ch][0];
			fCaliIntercept0[ch] = np->CalibrationCoef[iso_idx_0][ch][1];
			fCaliSlope1[ch] = np->CalibrationCoef[iso_idx_1][ch][0];
			fCaliIntercept1[ch] = np->CalibrationCoef[iso_idx_1][ch][1];
		}

		int w0 = ISO_val - ISO_val_0;
		int w1 = ISO_val_1 - ISO_val;

		for (int ch = 0; ch < ISP_BAYER_CHN_NUM; ch++) {
			fCaliSlope[ch] = (fCaliSlope0[ch] * w1 + fCaliSlope1[ch] * w0) / (w0 + w1);
			fCaliIntercept[ch] = (fCaliIntercept0[ch] * w1 + fCaliIntercept1[ch] * w0) / (w0 + w1);
		}
	} else {
		for (int ch = 0; ch < ISP_BAYER_CHN_NUM; ch++) {
			fCaliSlope[ch] = np->CalibrationCoef[iso_idx_0][ch][0];
			fCaliIntercept[ch] = np->CalibrationCoef[iso_idx_0][ch][1];
		}
	}

	// BGGR channels x digital gain
	for (int n = 0; n < ISP_BAYER_CHN_NUM; n++) {
		fCaliSlope[n] = fCaliSlope[n] * dgain / 1024;
		fCaliIntercept[n] = fCaliIntercept[n] * dgain / 1024;
	}

	// B channel x wb Bgain
	fCaliSlope[0] = fCaliSlope[0] * wbGain_b / 1024;
	fCaliIntercept[0] = fCaliIntercept[0] * wbGain_b / 1024;
	// R channel x wb Rgain
	fCaliSlope[3] = fCaliSlope[3] * wbGain_r / 1024;
	fCaliIntercept[3] = fCaliIntercept[3] * wbGain_r / 1024;

	fCaliSlopeG = (fCaliSlope[1] + fCaliSlope[2]) / 2;
	fCaliInterceptG = (fCaliIntercept[1] + fCaliIntercept[2]) / 2;
	fCaliSlopeB = fCaliSlope[0];
	fCaliSlopeR = fCaliSlope[3];
	fCaliInterceptB = fCaliIntercept[0];
	fCaliInterceptR = fCaliIntercept[3];

	for (int n = 0; n < YNR_CORING_NUM; n++) {
		uint16_t NpLumaThrBase_12b = MIN(NpLumaThrBase[n] * 16, 4095);
		uint16_t noise_level_R = (uint16_t)MAX(MIN(fCaliSlopeR * NpLumaThrBase_12b + fCaliInterceptR, 4095), 0);
		uint16_t noise_level_G = (uint16_t)MAX(MIN(fCaliSlopeG * NpLumaThrBase_12b + fCaliInterceptG, 4095), 0);
		uint16_t noise_level_B = (uint16_t)MAX(MIN(fCaliSlopeB * NpLumaThrBase_12b + fCaliInterceptB, 4095), 0);

		noise_level_R = (uint16_t)MAX(
			MIN((fCCM[0] * noise_level_R + fCCM[1] * noise_level_G + fCCM[2] * noise_level_B), 4095), 0);
		noise_level_G = (uint16_t)MAX(
			MIN((fCCM[3] * noise_level_R + fCCM[4] * noise_level_G + fCCM[5] * noise_level_B), 4095), 0);
		noise_level_B = (uint16_t)MAX(
			MIN((fCCM[6] * noise_level_R + fCCM[7] * noise_level_G + fCCM[8] * noise_level_B), 4095), 0);

		uint16_t noise_level_luma = (uint16_t)MAX(
			MIN((0.299 * noise_level_R + 0.587 * noise_level_G + 0.114 * noise_level_B), 4095), 0);

		float ratio = (float)GammaLUT[(uint8_t)(NpLumaThrBase_12b / 16)] / NpLumaThrBase_12b;

		NpOffsetThrBase[n] = (int)(ratio * noise_level_luma / 16);
	}

	for (int n = 0; n < YNR_CORING_NUM-1 ; n++) {
		if ((NpLumaThrBase[n + 1] - NpLumaThrBase[n]) != 0) {
			int slope = (NpOffsetThrBase[n + 1] - NpOffsetThrBase[n]) * 512 /
				(NpLumaThrBase[n + 1] - NpLumaThrBase[n]);

			slope = (slope > 1023) ? 1023 : slope; // 11-bit
			slope = (slope < -1024) ? -1024 : slope;
			NpSlopeBase[n] = slope;
		} else {
			NpSlopeBase[n] = 0;
		}
	}

	return 0;
}

static int np_ratio_calc(int NpRatio[NOISE_PROFILE_ISO_NUM], int ISO_val)
{
	int np_ratio = 0;
	CVI_U32 *ISO_list;

	get_iso_tbl(&ISO_list);

	int iso_idx_0 = MIN(FindPower2((int)(ISO_val / 100)), 15);
	int iso_idx_1 = (iso_idx_0 >= 15) ? iso_idx_0 : iso_idx_0 + 1;
	int ISO_val_0 = ISO_list[iso_idx_0];
	int ISO_val_1 = ISO_list[iso_idx_1];

	if (iso_idx_0 < 15) {
		int w0 = ISO_val - ISO_val_0;
		int w1 = ISO_val_1 - ISO_val;

		np_ratio = (NpRatio[iso_idx_0] * w1 + NpRatio[iso_idx_1] * w0) / (w0 + w1);
	} else {
		np_ratio = NpRatio[iso_idx_0];
	}

	return np_ratio;
}

/**
 * @brief YNR: Obtain LUT for block matching
 *
 * @param[out] reg_ynr_weight_lut_h[64]
 * @param[in] YNR_CoarseStr [0, 255]
 */
static void YNR_LUT_H_API(CVI_U8 *reg_ynr_weight_lut_h, CVI_U16 YNR_CoarseStr)
{
	memcpy(reg_ynr_weight_lut_h, &global_ynr_weight_lut_h[YNR_CoarseStr * 64], sizeof(uint8_t) * 64);
}

/**
 * @brief YNR: Obtain filter kernel register values according to the input filter type
 *
 * @param YnrKernel[out]
 * @param FilterType[in] [0, 11]
 */
static void YNR_Kernel_API(YNR_KERNEL_S *YnrKernel, int FilterType)
{
	unsigned int wLUT[3] = { 0 };

	for (int i = 0; i < 3; i++)
		wLUT[i] = WeightIntraBlockLUT[FilterType * 3 + i];

	unsigned int sum_reg_ynr_weight_norm_intra2 = 0;
	unsigned int sum_reg_ynr_weight_norm_intra1 = 0;
	int num = 0;

	for (int i = 0; i < 3; i++) {
		if (i == 0) {
			num = 1;
			sum_reg_ynr_weight_norm_intra2 += (1 << wLUT[i]) * num; // for 5x5 block matching
			sum_reg_ynr_weight_norm_intra1 += (1 << wLUT[i]) * num; // for 3x3 block matching
		} else if (i == 1) {
			num = 8;
			sum_reg_ynr_weight_norm_intra2 += (1 << wLUT[i]) * num; // for 5x5 block matching
			sum_reg_ynr_weight_norm_intra1 += (1 << wLUT[i]) * num; // for 3x3 block matching
		} else {
			num = 16;
			sum_reg_ynr_weight_norm_intra2 += (1 << wLUT[i]) * num; // for 5x5 block matching
		}
	}

	YnrKernel->reg_ynr_weight_intra_0 = wLUT[0];
	YnrKernel->reg_ynr_weight_intra_1 = wLUT[1];
	YnrKernel->reg_ynr_weight_intra_2 = wLUT[2];

	YnrKernel->reg_ynr_weight_norm_intra2 = (1 << 13) / sum_reg_ynr_weight_norm_intra2;
	YnrKernel->reg_ynr_weight_norm_intra1 = (1 << 12) / sum_reg_ynr_weight_norm_intra1;
}

